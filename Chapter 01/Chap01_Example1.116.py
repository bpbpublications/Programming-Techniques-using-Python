print(float(31.33)) # -- F1
  
# for floating point numbers 
print(float(1))  # -- F2
  
# for integer type strings 
print(float("35"))  # -- F3
  
# for floating type strings 
print(float("-69.69"))  # -- F4
  
# for string floats with whitespaces 
print(float("          -35.56   \n")) # -- F5 
  
# for inf/infinity 
print(float("inf"))  # -- F6
print(float("InfiNITy"))  # -- F7
  
# for NaN 
print(float("nan"))  # -- F8
print(float("NAN"))  # -- F9