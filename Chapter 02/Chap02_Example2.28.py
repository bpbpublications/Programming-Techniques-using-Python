#isalnum
s1 = 'HelloBrother'
print(s1.isalnum()) # -- IS1
s2 = "Hello Brother"
print(s2.isalnum()) # -- IS2
s3 = "indigo@1234"
print(s3.isalnum()) # -- IS3
s4 = "Dial100"
print(s4.isalnum()) # -- IS4
s5 = "#1234"
print(s5.isalnum()) # -- IS5
s_5 = "Hello()"
print(s_5.isalnum()) # -- IS6
s6 = "!Hello"
print(s6.isalnum()) # -- IS7
s7 = "@Hello"
print(s7.isalnum()) # -- IS8
s8 = "\$Hello"
print(s8.isalnum()) # -- IS9
s9 = "%Hello"
print(s9.isalnum()) # -- IS10
s10 = "^Hello"
print(s10.isalnum()) # -- IS11
s11 = "&Hello"
print(s11.isalnum()) # -- IS12
s12 = "?Hello"
print(s12.isalnum()) # -- IS13