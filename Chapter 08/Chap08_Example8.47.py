class Mymaths:
    @staticmethod
    def sum(a,b):
        print(f"The sum of {a} + {b} is ", a+b)
    
    @staticmethod
    def difference(a,b):
        print(f"The difference of {a} - {b} is ", a-b)
    
    @staticmethod
    def multiplication(a,b):
        print(f"The multiplication of {a} * {b} is ", a*b)
    
    @staticmethod
    def division(a,b):
        print(f"The division of {a}/{b} is ", a/b)

Mymaths.sum(15,12)
Mymaths.difference(15,12)
Mymaths.multiplication(15,12)
Mymaths.division(15,12)